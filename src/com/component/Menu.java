    public void initMenu(EventMenu event) {
        this.event = event;
        addMenu(new ImageIcon(getClass().getResource("/com/icon/1.png")), "Dashboard", 0);
        addMenu(new ImageIcon(getClass().getResource("/com/icon/2.png")), "Releasing And Importings", 1);
        addMenu(new ImageIcon(getClass().getResource("/com/icon/3.png")), "Registers", 2);
        addMenu(new ImageIcon(getClass().getResource("/com/icon/4.png")), "Attendence And Salary", 3);
        addMenu(new ImageIcon(getClass().getResource("/com/icon/5.png")), "Other", 4);
        addMenu(new ImageIcon(getClass().getResource("/com/icon/2.png")), "Reports", 5);
        addEmpty();
        addMenu(new ImageIcon(getClass().getResource("/com/icon/logout.png")), "Logout", 8);
    }

    private void addEmpty() {
        panelMenu.add(new JLabel(), "push");
    }

    private void addMenu(Icon icon, String text, int index) {
        ButtonMenu menu = new ButtonMenu();
        menu.setIcon(icon);
        menu.setText("  " + text);
        Font font = new Font("Quicksand",Font.PLAIN, 12);
        menu.setFont(font);
        panelMenu.add(menu);
        menu.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                event.selected(index);
                setSelected(menu);
            }
        });
    }

    private void setSelected(ButtonMenu menu) {
        for (Component com : panelMenu.getComponents()) {
            if (com instanceof ButtonMenu) {
                ButtonMenu b = (ButtonMenu) com;
                b.setSelected(false);
            }
        }
        menu.setSelected(true);
    }

