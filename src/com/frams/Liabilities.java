    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String liabilities = String.valueOf(jComboBox1.getSelectedItem());
        Double amount = Double.valueOf(jFormattedTextField1.getText());
        Double interestRate = Double.valueOf(jFormattedTextField2.getText());
        String description = String.valueOf(jTextField2.getText());

        if (liabilities == "SELECT") {
            JOptionPane.showMessageDialog(this, "Please Selete Expense", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (amount == 0.0) {
            JOptionPane.showMessageDialog(this, "Please Enter Amount", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            int liabilitiesId = liabilitiesMap.get(liabilities);

            LocalDate d = LocalDate.now();
            DateTimeFormatter f = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            String date = d.format(f);
            try {
                MySql.execute("INSERT INTO `liabilities_details` (`date`,`amount`,`interest_rate`,`description`,`liabilities_lia_id`) VALUES('" + date + "','" + amount + "','"+interestRate+"','"+description+"','" + liabilitiesId + "')");
                clearLiabilities();
                loadLiabilitiesDetails();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

