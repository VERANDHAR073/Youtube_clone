    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String name = String.valueOf(jTextField1.getText());
        String code = String.valueOf(jTextField3.getText());

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet resultSet = MySql.execute("SELECT * FROM `liabilities` WHERE `lia_name` = '" + name + "' OR `lia_code` = '" + code + "'");
                if (!resultSet.next()) {
                    MySql.execute("INSERT INTO `liabilities` (`lia_code`,`lia_name`)VALUES('" + code + "','" + name + "')");
                    loadLiabilities();
                    clearLiabilitiesRegister();
                } else {
                    JOptionPane.showMessageDialog(this, "Name Or Code Already Used", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

