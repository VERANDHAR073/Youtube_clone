    private void loadLiabilities() {
        try {
            ResultSet resultSet = MySql.execute("SELECT * FROM `liabilities`");
            Vector fv = new Vector();
            fv.add("SELECT");

            while (resultSet.next()) {
                fv.add(resultSet.getString("lia_name"));
                liabilitiesMap.put(resultSet.getString("lia_name"), resultSet.getInt("lia_id"));
            }

            DefaultComboBoxModel cb = new DefaultComboBoxModel(fv);
            jComboBox1.setModel(cb);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadLiabilitiesDetails() {
        try {
            ResultSet resultSet = MySql.execute("SELECT * FROM `liabilities_details` INNER JOIN `liabilities` ON `liabilities_details`.`liabilities_lia_id` = `liabilities`.`lia_id`");

            DefaultTableModel tm = (DefaultTableModel) jTable2.getModel();
            tm.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> pv = new Vector<>();
                pv.add(resultSet.getString("liad_id"));
                pv.add(resultSet.getString("lia_name"));
                pv.add(resultSet.getString("date"));
                pv.add(resultSet.getString("amount"));
                pv.add(resultSet.getString("description"));
                pv.add(resultSet.getString("interest_rate"));

                tm.addRow(pv);
                jTable2.setModel(tm);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clearLiabilitiesRegister() {
        jTextField3.setText("");
        jTextField1.setText("");
        loadLiabilities();
    }

