    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = jTextField1.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet resultSet = MySql.execute("SELECT * FROM `category` WHERE `category` = '" + name + "'");
                if (!resultSet.next()) {
                    MySql.execute("INSERT INTO `category` (`category`) VALUES('" + name + "')");
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Already have that brand", "Warning", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception e) {
                Logs logs = new Logs();
                logs.logger.log(Level.WARNING, "Category Register Fail");
                logs.logger.log(Level.WARNING, String.valueOf(e));
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int selectedRow = jTable1.getSelectedRow();
            String categoryId = String.valueOf(jTable1.getValueAt(selectedRow, 0));
            this.categoryId = categoryId;
            clear();
            jTextField1.setText(String.valueOf(jTable1.getValueAt(selectedRow, 1)));
            jButton5.setVisible(true);
            jButton2.setVisible(false);
        }
    }//GEN-LAST:event_jTable1MouseClicked

